 #include <iostream> 
  using namespace std; 
  int main() 
    { 
          int n = 7, k = 8; 
            cout << ++n << endl; 
            cout << n << endl; 
            cout << n++ << endl;
            cout << n << endl;
            cout << -n    << endl; 
            cout << n     << endl; 
            cout << --n << endl;
            cout << n << endl; 
            cout << n-- << endl;
            cout << n << endl;
            cout << n + k << endl;
            cout <<    n    << endl;
            cout <<    k    << endl; 
            cout << n << " "<< k << endl; 
            cout << "n" << endl; 
            cout << " n * n = "; 
            cout << n * n << endl; 
  } 